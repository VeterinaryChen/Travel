<template>
  <div>
    <detail-banner 
    :sightName="this.sightName" 
    :bannerImg="this.bannerImg"
    :bannerImgs="this.galleryImgs"
    >
    </detail-banner>
    <detail-header></detail-header>
    <div class="content">
      <detail-list :list="list"></detail-list>
    </div>
  </div>
</template>

<script>
import DetailBanner from './components/DetailBanner';
import DetailHeader from './components/Header';
import DetailList from './components/List';
import axios from 'axios';
export default { 
  name: 'Detail',
  components: {
    DetailBanner,
    DetailHeader,
    DetailList
  },
  data(){
    return{
      sightName:'',
      bannerImg:'',
      galleryImgs:[],
      list:[]
    }
  },
  methods:{
    getDetailInfo(){
      axios.get('/api/detail.json',{
        params:{
          id:this.$route.params.id
        }
      }).then(this.handleGetDataSuccess)
  },
  handleGetDataSuccess(res){
    res = res.data;
    if(res.ret && res.data){
      const data = res.data;
      this.sightName = data.sightName;
      this.bannerImg = data.bannerImg;
      this.galleryImgs = data.gallaryImgs;
      this.list = data.categoryList;
      console.log(data);
    }
  }
  
  },
  mounted(){
    this.getDetailInfo();
  },

}
</script>

